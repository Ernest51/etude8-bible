def get_verse_by_verse_content(book: str, chapter: int) -> dict:
    """Récupère le contenu verset par verset pour un livre et chapitre donné"""
    return VERSE_BY_VERSE_LIBRARY.get(book, {}).get(chapter, {})

def get_all_verses_for_chapter(book: str, chapter: int) -> list:
    """Récupère tous les versets d'un chapitre avec leurs explications"""
    chapter_content = get_verse_by_verse_content(book, chapter)
    verses = []
    
    for verse_num in sorted(chapter_content.keys()):
        verse_data = chapter_content[verse_num]
        verses.append({
            "verse_number": verse_num,
            "verse_text": verse_data["verse"],
            "explanation": verse_data["explanation"]
        })
    
    return verses